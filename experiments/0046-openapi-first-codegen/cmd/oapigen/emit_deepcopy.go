package main

import (
	"fmt"
	"strings"
)

// emitDeepcopy generates deepcopy methods for the external types. The
// output mirrors deepcopy-gen's shape closely enough that explain/SSA
// and the runtime.Object interface are satisfied.
func emitDeepcopy(m *Model) string {
	c := m.Config
	var b strings.Builder
	b.WriteString("//go:build !ignore_autogenerated\n")
	b.WriteString("// +build !ignore_autogenerated\n\n")
	b.WriteString(generatedHeader)
	b.WriteString("\n")
	fmt.Fprintf(&b, "package %s\n\n", c.Package)
	b.WriteString("import (\n\truntime \"k8s.io/apimachinery/pkg/runtime\"\n)\n\n")

	// Top-level Kind.
	emitKindDeepcopy(&b, m)
	emitListDeepcopy(&b, m)

	// Structs.
	for _, s := range m.Structs {
		emitStructDeepcopy(&b, s)
	}
	return b.String()
}

func emitKindDeepcopy(b *strings.Builder, m *Model) {
	k := m.Config.Kind
	fmt.Fprintf(b, "// DeepCopyInto copies the receiver into out.\n")
	fmt.Fprintf(b, "func (in *%s) DeepCopyInto(out *%s) {\n", k, k)
	b.WriteString("\t*out = *in\n")
	b.WriteString("\tout.TypeMeta = in.TypeMeta\n")
	b.WriteString("\tin.ObjectMeta.DeepCopyInto(&out.ObjectMeta)\n")
	if m.SpecType != "" {
		b.WriteString("\tin.Spec.DeepCopyInto(&out.Spec)\n")
	}
	if m.StatusType != "" {
		b.WriteString("\tin.Status.DeepCopyInto(&out.Status)\n")
	}
	b.WriteString("}\n\n")

	fmt.Fprintf(b, "// DeepCopy creates a new %s.\n", k)
	fmt.Fprintf(b, "func (in *%s) DeepCopy() *%s {\n", k, k)
	b.WriteString("\tif in == nil {\n\t\treturn nil\n\t}\n")
	fmt.Fprintf(b, "\tout := new(%s)\n", k)
	b.WriteString("\tin.DeepCopyInto(out)\n\treturn out\n}\n\n")

	fmt.Fprintf(b, "// DeepCopyObject implements runtime.Object.\n")
	fmt.Fprintf(b, "func (in *%s) DeepCopyObject() runtime.Object {\n", k)
	b.WriteString("\tif c := in.DeepCopy(); c != nil {\n\t\treturn c\n\t}\n\treturn nil\n}\n\n")
}

func emitListDeepcopy(b *strings.Builder, m *Model) {
	lk := m.Config.listKind()
	k := m.Config.Kind
	fmt.Fprintf(b, "// DeepCopyInto copies the receiver into out.\n")
	fmt.Fprintf(b, "func (in *%s) DeepCopyInto(out *%s) {\n", lk, lk)
	b.WriteString("\t*out = *in\n")
	b.WriteString("\tout.TypeMeta = in.TypeMeta\n")
	b.WriteString("\tin.ListMeta.DeepCopyInto(&out.ListMeta)\n")
	b.WriteString("\tif in.Items != nil {\n")
	b.WriteString("\t\tin, out := &in.Items, &out.Items\n")
	fmt.Fprintf(b, "\t\t*out = make([]%s, len(*in))\n", k)
	b.WriteString("\t\tfor i := range *in {\n\t\t\t(*in)[i].DeepCopyInto(&(*out)[i])\n\t\t}\n")
	b.WriteString("\t}\n}\n\n")

	fmt.Fprintf(b, "// DeepCopy creates a new %s.\n", lk)
	fmt.Fprintf(b, "func (in *%s) DeepCopy() *%s {\n", lk, lk)
	b.WriteString("\tif in == nil {\n\t\treturn nil\n\t}\n")
	fmt.Fprintf(b, "\tout := new(%s)\n", lk)
	b.WriteString("\tin.DeepCopyInto(out)\n\treturn out\n}\n\n")

	fmt.Fprintf(b, "// DeepCopyObject implements runtime.Object.\n")
	fmt.Fprintf(b, "func (in *%s) DeepCopyObject() runtime.Object {\n", lk)
	b.WriteString("\tif c := in.DeepCopy(); c != nil {\n\t\treturn c\n\t}\n\treturn nil\n}\n\n")
}

func emitStructDeepcopy(b *strings.Builder, s *GoStruct) {
	fmt.Fprintf(b, "// DeepCopyInto copies the receiver into out.\n")
	fmt.Fprintf(b, "func (in *%s) DeepCopyInto(out *%s) {\n", s.Name, s.Name)
	b.WriteString("\t*out = *in\n")
	for _, f := range s.Fields {
		emitFieldDeepcopy(b, f)
	}
	b.WriteString("}\n\n")

	fmt.Fprintf(b, "// DeepCopy creates a new %s.\n", s.Name)
	fmt.Fprintf(b, "func (in *%s) DeepCopy() *%s {\n", s.Name, s.Name)
	b.WriteString("\tif in == nil {\n\t\treturn nil\n\t}\n")
	fmt.Fprintf(b, "\tout := new(%s)\n", s.Name)
	b.WriteString("\tin.DeepCopyInto(out)\n\treturn out\n}\n\n")
}

// emitFieldDeepcopy emits the per-field deep copy logic inside
// DeepCopyInto. The leading "*out = *in" already copies value-type
// fields; we only need to handle pointers, slices, maps, structs, and
// metav1.Time.
func emitFieldDeepcopy(b *strings.Builder, f GoField) {
	name := f.GoName
	switch {
	case f.GoType == "metav1.Time" || f.GoType == "*metav1.Time":
		// metav1.Time has a DeepCopyInto; pointer form needs a guard.
		if f.Pointer {
			fmt.Fprintf(b, "\tif in.%s != nil {\n", name)
			b.WriteString("\t\tin, out := &in." + name + ", &out." + name + "\n")
			b.WriteString("\t\t*out = (*in).DeepCopy()\n")
			b.WriteString("\t}\n")
		} else {
			fmt.Fprintf(b, "\tin.%s.DeepCopyInto(&out.%s)\n", name, name)
		}
	case f.Pointer && isScalarBase(f.GoType):
		// *int32, *string, etc.
		fmt.Fprintf(b, "\tif in.%s != nil {\n", name)
		b.WriteString("\t\tin, out := &in." + name + ", &out." + name + "\n")
		base := strings.TrimPrefix(f.GoType, "*")
		fmt.Fprintf(b, "\t\t*out = new(%s)\n", base)
		b.WriteString("\t\t**out = **in\n")
		b.WriteString("\t}\n")
	case f.Pointer && f.RefName != "":
		// *Struct
		fmt.Fprintf(b, "\tif in.%s != nil {\n", name)
		b.WriteString("\t\tin, out := &in." + name + ", &out." + name + "\n")
		fmt.Fprintf(b, "\t\t*out = new(%s)\n", f.RefName)
		b.WriteString("\t\t(*in).DeepCopyInto(*out)\n")
		b.WriteString("\t}\n")
	case strings.HasPrefix(f.GoType, "[]"):
		elem := strings.TrimPrefix(f.GoType, "[]")
		fmt.Fprintf(b, "\tif in.%s != nil {\n", name)
		b.WriteString("\t\tin, out := &in." + name + ", &out." + name + "\n")
		fmt.Fprintf(b, "\t\t*out = make([]%s, len(*in))\n", elem)
		if f.ElemKind == "ref" {
			b.WriteString("\t\tfor i := range *in {\n\t\t\t(*in)[i].DeepCopyInto(&(*out)[i])\n\t\t}\n")
		} else {
			b.WriteString("\t\tcopy(*out, *in)\n")
		}
		b.WriteString("\t}\n")
	case strings.HasPrefix(f.GoType, "map[string]interface{}"):
		fmt.Fprintf(b, "\tif in.%s != nil {\n", name)
		b.WriteString("\t\tin, out := &in." + name + ", &out." + name + "\n")
		b.WriteString("\t\t*out = make(map[string]interface{}, len(*in))\n")
		b.WriteString("\t\tfor key, val := range *in {\n\t\t\t(*out)[key] = val\n\t\t}\n")
		b.WriteString("\t}\n")
	case strings.HasPrefix(f.GoType, "map[string]"):
		valType := strings.TrimPrefix(f.GoType, "map[string]")
		fmt.Fprintf(b, "\tif in.%s != nil {\n", name)
		b.WriteString("\t\tin, out := &in." + name + ", &out." + name + "\n")
		fmt.Fprintf(b, "\t\t*out = make(map[string]%s, len(*in))\n", valType)
		b.WriteString("\t\tfor key, val := range *in {\n\t\t\t(*out)[key] = val\n\t\t}\n")
		b.WriteString("\t}\n")
	case f.RefName != "" && !f.Pointer:
		// embedded named struct (value)
		fmt.Fprintf(b, "\tin.%s.DeepCopyInto(&out.%s)\n", name, name)
	default:
		// scalar value, enum: already handled by *out = *in.
	}
}

func isScalarBase(goType string) bool {
	switch strings.TrimPrefix(goType, "*") {
	case "string", "int32", "int64", "float32", "float64", "bool":
		return true
	}
	return false
}
