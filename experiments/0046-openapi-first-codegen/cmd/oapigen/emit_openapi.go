package main

import (
	"fmt"
	"sort"
	"strings"
)

// emitOpenAPI generates the openapi-gen-shaped GetOpenAPIDefinitions.
// It merges the substrate's baseline meta/v1 definitions
// (runtime/component/v2/openapi.BaselineDefinitions) with the generated
// type definitions, each carrying the x-kubernetes-group-version-kind
// extension, ObjectMeta/ListMeta ref-callbacks, and a Dependencies list.
//
// Definition map keys are canonical Go type names
// (<goPackagePath>.<Type>); the apiserver's openapi.NewDefinitionNamer
// maps them to the reverse-DNS friendly names kubectl explain indexes.
func emitOpenAPI(m *Model) string {
	c := m.Config
	pkgPath := c.GoPackagePath
	var b strings.Builder
	b.WriteString("//go:build !ignore_autogenerated\n")
	b.WriteString("// +build !ignore_autogenerated\n\n")
	b.WriteString(generatedHeader)
	b.WriteString("\n// Code generated by openapi-gen. DO NOT EDIT.\n\n")
	fmt.Fprintf(&b, "package %s\n\n", c.Package)
	b.WriteString("import (\n")
	b.WriteString("\tcommon \"k8s.io/kube-openapi/pkg/common\"\n")
	b.WriteString("\tspec \"k8s.io/kube-openapi/pkg/validation/spec\"\n\n")
	b.WriteString("\tbaseline \"github.com/cheeseandcereal/aggexp/runtime/component/v2/openapi\"\n")
	b.WriteString(")\n\n")

	// GetOpenAPIDefinitions: merge baseline + generated.
	b.WriteString("// GetOpenAPIDefinitions returns the OpenAPI definitions for this API\n")
	b.WriteString("// package, merged with the substrate's baseline meta/v1 definitions.\n")
	b.WriteString("func GetOpenAPIDefinitions(ref common.ReferenceCallback) map[string]common.OpenAPIDefinition {\n")
	b.WriteString("\tdefs := baseline.BaselineDefinitions(ref)\n")
	emitDefAssign(&b, pkgPath, c.Kind, "schema_"+sanitize(c.Kind))
	emitDefAssign(&b, pkgPath, c.listKind(), "schema_"+sanitize(c.listKind()))
	for _, s := range m.Structs {
		emitDefAssign(&b, pkgPath, s.Name, "schema_"+sanitize(s.Name))
	}
	b.WriteString("\treturn defs\n}\n\n")

	// gvkExtension helper.
	b.WriteString("// gvkExtension builds the x-kubernetes-group-version-kind extension\n")
	b.WriteString("// map kubectl explain indexes by and managedfields.NewTypeConverter\n")
	b.WriteString("// requires to key the type (FINDINGS/0002, FINDINGS/0017).\n")
	b.WriteString("func gvkExtension(group, version, kind string) spec.Extensions {\n")
	b.WriteString("\treturn spec.Extensions{\n")
	b.WriteString("\t\t\"x-kubernetes-group-version-kind\": []interface{}{\n")
	b.WriteString("\t\t\tmap[string]interface{}{\"group\": group, \"version\": version, \"kind\": kind},\n")
	b.WriteString("\t\t},\n\t}\n}\n\n")

	// Top-level Kind def.
	emitKindDef(&b, m)
	emitListDef(&b, m)
	for _, s := range m.Structs {
		emitStructDef(&b, m, s)
	}
	return b.String()
}

func emitDefAssign(b *strings.Builder, pkgPath, typeName, fn string) {
	fmt.Fprintf(b, "\tdefs[%q] = %s(ref)\n", pkgPath+"."+typeName, fn)
}

func sanitize(name string) string {
	return name
}

const objectMetaRef = "k8s.io/apimachinery/pkg/apis/meta/v1.ObjectMeta"
const listMetaRef = "k8s.io/apimachinery/pkg/apis/meta/v1.ListMeta"

func emitKindDef(b *strings.Builder, m *Model) {
	c := m.Config
	fn := "schema_" + sanitize(c.Kind)
	deps := []string{objectMetaRef}
	fmt.Fprintf(b, "func %s(ref common.ReferenceCallback) common.OpenAPIDefinition {\n", fn)
	b.WriteString("\treturn common.OpenAPIDefinition{\n")
	b.WriteString("\t\tSchema: spec.Schema{\n")
	b.WriteString("\t\t\tVendorExtensible: spec.VendorExtensible{Extensions: gvkExtension(GroupName, ")
	fmt.Fprintf(b, "%q, %q)},\n", c.Version, c.Kind)
	b.WriteString("\t\t\tSchemaProps: spec.SchemaProps{\n")
	fmt.Fprintf(b, "\t\t\t\tDescription: %q,\n", fmt.Sprintf("%s is the external (%s/%s) form of the resource.", c.Kind, c.Group, c.Version))
	b.WriteString("\t\t\t\tType:        []string{\"object\"},\n")
	b.WriteString("\t\t\t\tProperties: map[string]spec.Schema{\n")
	emitKindStrProp(b, "kind", "Kind is a string value representing the REST resource this object represents.")
	emitKindStrProp(b, "apiVersion", "APIVersion defines the versioned schema of this representation of an object.")
	emitRefProp(b, "metadata", objectMetaRef)
	if m.SpecType != "" {
		specKey := m.Config.GoPackagePath + "." + m.SpecType
		emitRefPropPkg(b, "spec", specKey)
		deps = append(deps, specKey)
	}
	if m.StatusType != "" {
		statusKey := m.Config.GoPackagePath + "." + m.StatusType
		emitRefPropPkg(b, "status", statusKey)
		deps = append(deps, statusKey)
	}
	b.WriteString("\t\t\t\t},\n")
	b.WriteString("\t\t\t},\n")
	b.WriteString("\t\t},\n")
	emitDependencies(b, deps)
	b.WriteString("\t}\n}\n\n")
}

func emitListDef(b *strings.Builder, m *Model) {
	c := m.Config
	fn := "schema_" + sanitize(c.listKind())
	itemKey := c.GoPackagePath + "." + c.Kind
	deps := []string{itemKey, listMetaRef}
	fmt.Fprintf(b, "func %s(ref common.ReferenceCallback) common.OpenAPIDefinition {\n", fn)
	b.WriteString("\treturn common.OpenAPIDefinition{\n")
	b.WriteString("\t\tSchema: spec.Schema{\n")
	b.WriteString("\t\t\tVendorExtensible: spec.VendorExtensible{Extensions: gvkExtension(GroupName, ")
	fmt.Fprintf(b, "%q, %q)},\n", c.Version, c.listKind())
	b.WriteString("\t\t\tSchemaProps: spec.SchemaProps{\n")
	fmt.Fprintf(b, "\t\t\t\tDescription: %q,\n", fmt.Sprintf("%s is a list of %s objects.", c.listKind(), c.Kind))
	b.WriteString("\t\t\t\tType:        []string{\"object\"},\n")
	b.WriteString("\t\t\t\tProperties: map[string]spec.Schema{\n")
	emitKindStrProp(b, "kind", "Kind is a string value representing the REST resource this object represents.")
	emitKindStrProp(b, "apiVersion", "APIVersion defines the versioned schema of this representation of an object.")
	emitRefProp(b, "metadata", listMetaRef)
	// items array.
	b.WriteString("\t\t\t\t\t\"items\": {\n")
	b.WriteString("\t\t\t\t\t\tSchemaProps: spec.SchemaProps{\n")
	b.WriteString("\t\t\t\t\t\t\tType: []string{\"array\"},\n")
	b.WriteString("\t\t\t\t\t\t\tItems: &spec.SchemaOrArray{\n")
	b.WriteString("\t\t\t\t\t\t\t\tSchema: &spec.Schema{\n")
	fmt.Fprintf(b, "\t\t\t\t\t\t\t\t\tSchemaProps: spec.SchemaProps{Default: map[string]interface{}{}, Ref: ref(%q)},\n", itemKey)
	b.WriteString("\t\t\t\t\t\t\t\t},\n")
	b.WriteString("\t\t\t\t\t\t\t},\n")
	b.WriteString("\t\t\t\t\t\t},\n")
	b.WriteString("\t\t\t\t\t},\n")
	b.WriteString("\t\t\t\t},\n")
	b.WriteString("\t\t\t\tRequired: []string{\"items\"},\n")
	b.WriteString("\t\t\t},\n")
	b.WriteString("\t\t},\n")
	emitDependencies(b, deps)
	b.WriteString("\t}\n}\n\n")
}

func emitStructDef(b *strings.Builder, m *Model, s *GoStruct) {
	fn := "schema_" + sanitize(s.Name)
	var deps []string
	fmt.Fprintf(b, "func %s(ref common.ReferenceCallback) common.OpenAPIDefinition {\n", fn)
	b.WriteString("\treturn common.OpenAPIDefinition{\n")
	b.WriteString("\t\tSchema: spec.Schema{\n")
	b.WriteString("\t\t\tSchemaProps: spec.SchemaProps{\n")
	if s.Description != "" {
		fmt.Fprintf(b, "\t\t\t\tDescription: %q,\n", s.Description)
	}
	b.WriteString("\t\t\t\tType:        []string{\"object\"},\n")
	b.WriteString("\t\t\t\tProperties: map[string]spec.Schema{\n")
	var required []string
	for _, f := range s.Fields {
		d := emitFieldDef(b, m, f)
		deps = appendUnique(deps, d...)
		if f.Required {
			required = append(required, f.JSONName)
		}
	}
	b.WriteString("\t\t\t\t},\n")
	if len(required) > 0 {
		b.WriteString("\t\t\t\tRequired: []string{")
		for i, r := range required {
			if i > 0 {
				b.WriteString(", ")
			}
			fmt.Fprintf(b, "%q", r)
		}
		b.WriteString("},\n")
	}
	b.WriteString("\t\t\t},\n")
	b.WriteString("\t\t},\n")
	if len(deps) > 0 {
		sort.Strings(deps)
		emitDependencies(b, deps)
	}
	b.WriteString("\t}\n}\n\n")
}

// emitFieldDef emits one property schema and returns any def-map
// dependencies it introduces (refs).
func emitFieldDef(b *strings.Builder, m *Model, f GoField) []string {
	var deps []string
	indent := "\t\t\t\t\t"
	fmt.Fprintf(b, "%s%q: {\n", indent, f.JSONName)

	switch {
	case f.RefName != "" && f.ElemKind != "ref" && !strings.HasPrefix(f.GoType, "[]"):
		// $ref to a named struct (object or *object).
		refKey := m.Config.GoPackagePath + "." + f.RefName
		b.WriteString(indent + "\tSchemaProps: spec.SchemaProps{\n")
		if f.Description != "" {
			fmt.Fprintf(b, "%s\t\tDescription: %q,\n", indent, f.Description)
		}
		fmt.Fprintf(b, "%s\t\tRef: ref(%q),\n", indent, refKey)
		b.WriteString(indent + "\t},\n")
		deps = append(deps, refKey)
	case strings.HasPrefix(f.GoType, "[]"):
		b.WriteString(indent + "\tSchemaProps: spec.SchemaProps{\n")
		if f.Description != "" {
			fmt.Fprintf(b, "%s\t\tDescription: %q,\n", indent, f.Description)
		}
		b.WriteString(indent + "\t\tType: []string{\"array\"},\n")
		b.WriteString(indent + "\t\tItems: &spec.SchemaOrArray{\n")
		b.WriteString(indent + "\t\t\tSchema: &spec.Schema{\n")
		if f.ElemKind == "ref" {
			refKey := m.Config.GoPackagePath + "." + f.ElemRefName
			fmt.Fprintf(b, "%s\t\t\t\tSchemaProps: spec.SchemaProps{Default: map[string]interface{}{}, Ref: ref(%q)},\n", indent, refKey)
			deps = append(deps, refKey)
		} else {
			fmt.Fprintf(b, "%s\t\t\t\tSchemaProps: spec.SchemaProps{Type: []string{%q}, Format: %q},\n", indent, f.ElemOAPIType, f.ElemOAPIFormat)
		}
		b.WriteString(indent + "\t\t\t},\n")
		b.WriteString(indent + "\t\t},\n")
		b.WriteString(indent + "\t},\n")
	case f.FreeForm:
		b.WriteString(indent + "\tSchemaProps: spec.SchemaProps{\n")
		if f.Description != "" {
			fmt.Fprintf(b, "%s\t\tDescription: %q,\n", indent, f.Description)
		}
		b.WriteString(indent + "\t\tType: []string{\"object\"},\n")
		b.WriteString(indent + "\t},\n")
	case strings.HasPrefix(f.GoType, "map[string]"):
		b.WriteString(indent + "\tSchemaProps: spec.SchemaProps{\n")
		if f.Description != "" {
			fmt.Fprintf(b, "%s\t\tDescription: %q,\n", indent, f.Description)
		}
		b.WriteString(indent + "\t\tType: []string{\"object\"},\n")
		b.WriteString(indent + "\t\tAdditionalProperties: &spec.SchemaOrBool{\n")
		b.WriteString(indent + "\t\t\tAllows: true,\n")
		b.WriteString(indent + "\t\t\tSchema: &spec.Schema{\n")
		fmt.Fprintf(b, "%s\t\t\t\tSchemaProps: spec.SchemaProps{Type: []string{%q}, Format: %q},\n", indent, f.MapValueOAPIType, f.MapValueOAPIFormat)
		b.WriteString(indent + "\t\t\t},\n")
		b.WriteString(indent + "\t\t},\n")
		b.WriteString(indent + "\t},\n")
	default:
		// scalar / enum / time.
		b.WriteString(indent + "\tSchemaProps: spec.SchemaProps{\n")
		if f.Description != "" {
			fmt.Fprintf(b, "%s\t\tDescription: %q,\n", indent, f.Description)
		}
		fmt.Fprintf(b, "%s\t\tType: []string{%q},\n", indent, f.OAPIType)
		fmt.Fprintf(b, "%s\t\tFormat: %q,\n", indent, f.OAPIFormat)
		b.WriteString(indent + "\t},\n")
	}
	b.WriteString(indent + "},\n")
	return deps
}

func emitKindStrProp(b *strings.Builder, name, desc string) {
	indent := "\t\t\t\t\t"
	fmt.Fprintf(b, "%s%q: {\n", indent, name)
	b.WriteString(indent + "\tSchemaProps: spec.SchemaProps{\n")
	fmt.Fprintf(b, "%s\t\tDescription: %q,\n", indent, desc)
	b.WriteString(indent + "\t\tType: []string{\"string\"},\n")
	b.WriteString(indent + "\t},\n")
	b.WriteString(indent + "},\n")
}

// emitRefProp emits a property that $refs a meta type (ObjectMeta/ListMeta).
func emitRefProp(b *strings.Builder, name, refKey string) {
	indent := "\t\t\t\t\t"
	fmt.Fprintf(b, "%s%q: {\n", indent, name)
	b.WriteString(indent + "\tSchemaProps: spec.SchemaProps{\n")
	b.WriteString(indent + "\t\tDefault: map[string]interface{}{},\n")
	fmt.Fprintf(b, "%s\t\tRef: ref(%q),\n", indent, refKey)
	b.WriteString(indent + "\t},\n")
	b.WriteString(indent + "},\n")
}

// emitRefPropPkg emits a property that $refs a generated type in this pkg.
func emitRefPropPkg(b *strings.Builder, name, refKey string) {
	emitRefProp(b, name, refKey)
}

func emitDependencies(b *strings.Builder, deps []string) {
	b.WriteString("\t\tDependencies: []string{\n\t\t\t")
	for i, d := range deps {
		if i > 0 {
			b.WriteString(", ")
		}
		fmt.Fprintf(b, "%q", d)
	}
	b.WriteString("},\n")
}

func appendUnique(s []string, items ...string) []string {
	for _, it := range items {
		found := false
		for _, e := range s {
			if e == it {
				found = true
				break
			}
		}
		if !found {
			s = append(s, it)
		}
	}
	return s
}
