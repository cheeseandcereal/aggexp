package main

import (
	"fmt"
	"go/format"
	"os"
	"path/filepath"
	"strings"
)

// generatedHeader is the boilerplate that marks files as generated. The
// SPDX line matches the repo's deepcopy convention.
const generatedHeader = `// Code generated by oapigen (experiment 0046). DO NOT EDIT.
// SPDX-License-Identifier: Unlicense
`

// writeGoFile formats src as Go and writes it to outputDir/name.
func writeGoFile(outputDir, name, src string) error {
	formatted, err := format.Source([]byte(src))
	if err != nil {
		// Write the unformatted source to a sibling file for
		// debugging, then surface the error.
		_ = os.WriteFile(filepath.Join(outputDir, name+".broken"), []byte(src), 0o644)
		return fmt.Errorf("format %s: %w", name, err)
	}
	return os.WriteFile(filepath.Join(outputDir, name), formatted, 0o644)
}

// emit writes every generated file for the model into cfg.OutputDir.
func emit(m *Model) error {
	dir := m.Config.OutputDir
	if err := os.MkdirAll(dir, 0o755); err != nil {
		return fmt.Errorf("mkdir output: %w", err)
	}

	files := []struct {
		name string
		gen  func(*Model) string
	}{
		{"doc.go", emitDoc},
		{"types.go", emitTypes},
		{"zz_generated.deepcopy.go", emitDeepcopy},
		{"register.go", emitRegister},
		{"conversion.go", emitConversion},
		{"fieldlabels.go", emitFieldLabels},
		{"zz_generated.openapi.go", emitOpenAPI},
	}
	for _, f := range files {
		if err := writeGoFile(dir, f.name, f.gen(m)); err != nil {
			return err
		}
	}
	return nil
}

// ---- doc.go ----

func emitDoc(m *Model) string {
	c := m.Config
	var b strings.Builder
	b.WriteString(generatedHeader)
	b.WriteString("\n")
	fmt.Fprintf(&b, "// Package %s contains the generated external (%s/%s) types for\n", c.Package, c.Group, c.Version)
	fmt.Fprintf(&b, "// the %s resource. Generated by oapigen from an OpenAPI v3 document.\n", c.Kind)
	b.WriteString("//\n")
	fmt.Fprintf(&b, "// Input OpenAPI SHA-256: %s\n", m.OpenAPISHA256)
	b.WriteString("//\n")
	b.WriteString("// Do not edit this package by hand; regenerate with hack/gen.sh.\n")
	fmt.Fprintf(&b, "package %s\n", c.Package)
	return b.String()
}

// ---- types.go (external version) ----

func emitTypes(m *Model) string {
	c := m.Config
	var b strings.Builder
	b.WriteString(generatedHeader)
	b.WriteString("\n")
	fmt.Fprintf(&b, "package %s\n\n", c.Package)
	b.WriteString("import (\n\tmetav1 \"k8s.io/apimachinery/pkg/apis/meta/v1\"\n)\n\n")

	// Top-level Kind.
	b.WriteString("// +k8s:deepcopy-gen:interfaces=k8s.io/apimachinery/pkg/runtime.Object\n\n")
	fmt.Fprintf(&b, "// %s is the external (%s/%s) form of the resource.\n", c.Kind, c.Group, c.Version)
	fmt.Fprintf(&b, "type %s struct {\n", c.Kind)
	b.WriteString("\tmetav1.TypeMeta   `json:\",inline\"`\n")
	b.WriteString("\tmetav1.ObjectMeta `json:\"metadata,omitempty\"`\n")
	if m.SpecType != "" {
		fmt.Fprintf(&b, "\tSpec   %s `json:\"spec,omitempty\"`\n", m.SpecType)
	}
	if m.StatusType != "" {
		fmt.Fprintf(&b, "\tStatus %s `json:\"status,omitempty\"`\n", m.StatusType)
	}
	b.WriteString("}\n\n")

	// Top-level List.
	b.WriteString("// +k8s:deepcopy-gen:interfaces=k8s.io/apimachinery/pkg/runtime.Object\n\n")
	fmt.Fprintf(&b, "// %s is a list of %s objects.\n", c.listKind(), c.Kind)
	fmt.Fprintf(&b, "type %s struct {\n", c.listKind())
	b.WriteString("\tmetav1.TypeMeta `json:\",inline\"`\n")
	b.WriteString("\tmetav1.ListMeta `json:\"metadata,omitempty\"`\n\n")
	fmt.Fprintf(&b, "\tItems []%s `json:\"items\"`\n", c.Kind)
	b.WriteString("}\n\n")

	// Enums.
	for _, e := range m.Enums {
		emitEnum(&b, e)
	}

	// Named structs.
	for _, s := range m.Structs {
		emitStruct(&b, s)
	}
	return b.String()
}

func emitEnum(b *strings.Builder, e *GoEnum) {
	fmt.Fprintf(b, "// %s\n", e.Description)
	fmt.Fprintf(b, "type %s string\n\n", e.Name)
	b.WriteString("const (\n")
	for _, v := range e.Values {
		constName := e.Name + goExportName(v)
		fmt.Fprintf(b, "\t%s %s = %q\n", constName, e.Name, v)
	}
	b.WriteString(")\n\n")
}

func emitStruct(b *strings.Builder, s *GoStruct) {
	if s.Description != "" {
		fmt.Fprintf(b, "// %s\n", s.Description)
	}
	fmt.Fprintf(b, "type %s struct {\n", s.Name)
	for _, f := range s.Fields {
		if f.Description != "" {
			for _, line := range wrapComment(f.Description) {
				fmt.Fprintf(b, "\t// %s\n", line)
			}
		}
		tag := f.JSONName
		if f.OmitEmpty {
			tag += ",omitempty"
		}
		fmt.Fprintf(b, "\t%s %s `json:%q`\n", f.GoName, f.GoType, tag)
	}
	b.WriteString("}\n\n")
}

func wrapComment(s string) []string {
	s = strings.TrimSpace(s)
	if s == "" {
		return nil
	}
	return []string{s}
}
